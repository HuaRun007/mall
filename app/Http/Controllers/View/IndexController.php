    {
        $site_images = DB::table('site_image')->orderBy('sort')->get();
        $categorys = Category::get()->toArray();
        $member = $request->session()->get('member','');
        $index_category = Category::where('parent_id',0)->get();
        $products = Category::whereHas('product',function ($q){
            $q->where('product.on_sale', '1');
        })->get();
        return view('index',compact('site_images','categorys','member','index_category','products'));
    }

